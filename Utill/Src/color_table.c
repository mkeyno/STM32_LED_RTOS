RGB_Bit Table1(uint16_t speed,uint16_t span) {
    static uint16_t cnt1=0;
    static uint16_t spd=0;
    if(cnt1>359) {
        cnt1=0;
    }
    if(++spd==speed) {
        cnt1+=span;
        spd=0;
    }
    return COLOR_TABLE1[cnt1];
}

RGB_Bit Table2(uint8_t span) {
    static uint16_t cnt1=0;
    cnt1+=span;
    if(cnt1>359) {
        cnt1=0;
    }
    return COLOR_TABLE3[cnt1];
}

RGB_Bit Table3(void) {
    static uint16_t cnt1=0;
    if(cnt1==360) {
        cnt1=0;
    }
    return COLOR_TABLE3[cnt1++];
}

RGB_Bit RGB_Copy(RGB_Bit rgb) {
    return rgb;
}





